    VIEW_NAME
   ,REPLACE(DBMS_METADATA.GET_DDL('VIEW',VIEW_NAME,OWNER),'"') AS DMML
FROM
    ALL_VIEWS
WHERE
        OWNER = 'APPS'
    AND VIEW_NAME IN ( 'XXBM_SUPPLIERS_V', 'XXBM_SUPPLIERS_ADDRESSES_V');