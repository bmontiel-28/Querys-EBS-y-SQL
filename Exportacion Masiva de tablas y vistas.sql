SELECT
    TABLE_NAME
   ,REPLACE(DBMS_METADATA.GET_DDL('TABLE',TABLE_NAME,OWNER)
            ,'"') AS DMML
FROM
    ALL_TABLES
WHERE
        OWNER = 'APPS'
    AND TABLE_NAME IN ( 'XXBM_SUPPLIERS','XXBM_SUPPLIERS_INT','XXBM_SUPPLIERS_ADDRESSES','XXBM_SUPPLIERS_ADDRESSES_INT');

